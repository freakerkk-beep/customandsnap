/**
 * Dùng SVG do shop cung cấp, không dùng icon thay thế từ thư viện.
 * Tất cả SVG dùng `currentColor`, vì vậy màu icon tự đi theo màu chữ của bộ màu.
 */
const ICON_COMPONENTS: Record<IconId, ClickerIconComponent> = {
  heart: HeartIcon,
  star: StarIcon,
  flower: FlowerIcon,
  dog_feet: DogFeetIcon,
  lucky_leaf: LuckyLeafIcon,
  camera: assetIcon('camera.svg'),
  tennis: assetIcon('tennis.svg'),
  badminton: assetIcon('badminton.svg'),
  plane: assetIcon('plane.svg'),
  pickle: assetIcon('pickle.svg'),
  sun: assetIcon('sun.svg'),
  headphone: assetIcon('headphone.svg'),
  moon: assetIcon('moon.svg'),
  ribbon: assetIcon('ribbon.svg'),
};

export function getIconComponent(id: string): ClickerIconComponent | null {
  return ICON_COMPONENTS[id as IconId] ?? null;
}

export function isIconId(id: string): id is IconId {
  return id in ICON_COMPONENTS;
}

/** Nhãn in hoa dùng trong ghi chú đơn hàng: "lucky_leaf" -> "LUCKY LEAF". */
export function iconIdToNoteLabel(id: string): string {
  return id.replace(/_/g, ' ').toUpperCase();
}
